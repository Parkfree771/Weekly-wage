// 각인 아이콘 적용기.
// 소스 폴더(기본: 바탕화면 '각인')의 이미지 파일명을 각인 이름과 매칭해
//   1) public/engravings/ 로 '각인이름.확장자' 형태로 복사
//   2) lib/engraving-icons.generated.ts (각인명 → public 경로) 생성
//   3) 이미지 없는 각인 목록 출력
// 파일명이 공백/언더스코어/축약으로 제각각이라 정규화 + 별칭으로 흡수.
//
// 실행: node scripts/gen-engraving-icons.mjs ["소스폴더경로"]

import fs from 'node:fs';
import path from 'node:path';
import { fileURLToPath } from 'node:url';

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const ROOT = path.resolve(__dirname, '..');

// 생성된 빌드 파일을 텍스트로 파싱 (.ts import 회피)
const buildsTxt = fs.readFileSync(
  path.join(ROOT, 'lib', 'engraving-builds.generated.ts'),
  'utf8'
);
const ENGRAVING_BUILDS = {};
const reSpec = /"([^"]+)":\s*\{ slots:\s*(.+?),\s*stat:/g;
const parseArr = (s) => [...s.matchAll(/"([^"]+)"/g)].map((m) => m[1]);
let mm;
while ((mm = reSpec.exec(buildsTxt))) {
  ENGRAVING_BUILDS[mm[1]] = { names: parseArr(mm[2]) };
}
const SRC = process.argv[2] || 'C:/Users/park yu ro/Desktop/각인';
const OUT_DIR = path.join(ROOT, 'public', 'engravings');

// 페이지에서 숨기는 스펙은 아이콘 대상에서도 제외
const EXCLUDED_SPEC_IDS = new Set(['도화가 회귀', '바드 진용']);

// 축약/변형 파일명 별칭 (각인 이름 → 실제 파일 베이스명)
const ALIAS = { '기습의 대가': '기습' };

const norm = (s) => s.replace(/[\s_]/g, '');

// 표시되는 모든 각인(main+sub) 수집
const names = new Set();
for (const [id, b] of Object.entries(ENGRAVING_BUILDS)) {
  if (EXCLUDED_SPEC_IDS.has(id)) continue;
  b.names.forEach((n) => names.add(n));
}
const engNames = [...names].sort((a, b) => a.localeCompare(b, 'ko'));

// 소스 폴더 인덱싱: 정규화 베이스명 → 파일명 (.webp 우선)
const files = fs.readdirSync(SRC);
const fileMap = new Map();
for (const f of files) {
  const base = norm(f.replace(/\.(webp|png|jpe?g)$/i, ''));
  if (!fileMap.has(base) || /\.webp$/i.test(f)) fileMap.set(base, f);
}

fs.mkdirSync(OUT_DIR, { recursive: true });

const iconMap = {}; // 각인명 → /engravings/파일
const missing = [];
for (const n of engNames) {
  const key = norm(ALIAS[n] || n);
  const file = fileMap.get(key);
  if (!file) {
    missing.push(n);
    continue;
  }
  const ext = path.extname(file).toLowerCase();
  const destName = `${n}${ext}`; // 캐논 파일명 = 각인 이름
  fs.copyFileSync(path.join(SRC, file), path.join(OUT_DIR, destName));
  iconMap[n] = `/engravings/${encodeURIComponent(destName)}`;
}

const ordered = Object.keys(iconMap).sort((a, b) => a.localeCompare(b, 'ko'));
const body = ordered
  .map((n) => `  ${JSON.stringify(n)}: ${JSON.stringify(iconMap[n])},`)
  .join('\n');

const out = `// AUTO-GENERATED by scripts/gen-engraving-icons.mjs — 직접 수정 금지
// 각인 이름 → public 이미지 경로. 이미지 추가 시 스크립트 재실행.
export const ENGRAVING_ICONS: Record<string, string> = {
${body}
};
`;
fs.writeFileSync(path.join(ROOT, 'lib', 'engraving-icons.generated.ts'), out, 'utf8');

console.log(`적용 ${ordered.length}개 → public/engravings/, lib/engraving-icons.generated.ts`);
console.log(`이미지 없음 ${missing.length}개: ${missing.join(', ') || '없음'}`);
