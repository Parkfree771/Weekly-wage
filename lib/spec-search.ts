/** 스펙 id 첫 토큰(축약 직업명) → 정식 직업명 (DB class_name과 동일 표기) */
export function fullClassNameOf(specId: string): string {
  const token = specId.split(' ')[0];
  return CLASS_FULL_NAME[token] ?? token;
}

/** 스펙 하나의 검색 대상 문자열: "축약 id + 정식 직업명 + 스펙 풀네임" */
export function specSearchText(specId: string): string {
  return [specId, fullClassNameOf(specId), SPEC_FULL_NAME[specId]].filter(Boolean).join(' ');
}

/** 직업 옵션의 검색 대상 문자열: "정식 직업명 + 축약명" */
export function classSearchText(className: string): string {
  const abbrev = Object.entries(CLASS_FULL_NAME).find(([, full]) => full === className)?.[0];
  return [className, abbrev].filter(Boolean).join(' ');
}
