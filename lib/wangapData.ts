// 등급 시스템: 전설(0~15) → 유물(15~20) → 고대(20~25)
// +15 도달 시 유물 승급, +20 도달 시 고대 승급 필요
// 승급 재료는 벨가르딘 레이드 특수 재료 (이름·수량 미공개, 임시값)
// ========================================
export type WangapGrade = '전설' | '유물' | '고대';

export const WANGAP_GRADE_ORDER: WangapGrade[] = ['전설', '유물', '고대'];

// 등급별 강화 가능 구간 (min = 시작 레벨, max = 해당 등급 상한)
export const WANGAP_GRADE_RANGES: Record<WangapGrade, { min: number; max: number }> = {
  전설: { min: 0, max: 15 },
  유물: { min: 15, max: 20 },
  고대: { min: 20, max: 25 },
};

// 시작 단계 수동 조정 시 레벨로 등급 유추 (경계 레벨은 승급 완료 상태로 간주)
export const deriveWangapGrade = (level: number): WangapGrade => {
  if (level >= 20) return '고대';
  if (level >= 15) return '유물';
  return '전설';
};

