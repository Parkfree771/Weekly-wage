// 완갑 능력치 (임시 추정)
// 앵커: 공식 인증 +21 실측값 — 힘/민/지 52,381 / 체력 4,600 / 무기공격력 21,726 / 기본공격력 5,980
// (힘민지·체력 ≈ 방어구 5부위 평균의 40%, 무기공격력 ≈ 무기 증가량의 10% 수준으로 추정된 값)
// 성장 곡선: 등급 구간별 계단형 가중치 + 승급 시 점프,
//            "+25 풀강 시 강화로 얻는 총량 = +0 기본치" (즉 +25 = 기본의 2배) 가정으로 역산
// 실제 수치 공개 시 앵커·가중치만 교체하면 전 구간에 반영됨
// ========================================

export interface WangapStats {
  힘민지: number;
  체력: number;
  무기공격력: number;
  기본공격력: number;
}

export const WANGAP_FIXED_EFFECT = '기본 공격력 +2.0%';

const STAT_ANCHOR_LEVEL = 21;
const STAT_ANCHOR: WangapStats = { 힘민지: 52381, 체력: 4600, 무기공격력: 21726, 기본공격력: 5980 };

// 강당 성장 가중치 (target = 도달 레벨) — 고강일수록 증가폭 확대
const statWeight = (target: number): number => {
  if (target <= 5) return 1.0;
  if (target <= 10) return 1.4;
  if (target <= 15) return 1.8;
  if (target <= 20) return 2.4;
  return 3.0;
};

// 승급(유물/고대) 시 추가 점프
const PROMO_WEIGHT = 2.0;

// 등급 경계 레벨(15, 20)은 승급 전후 능력치가 다르므로 등급을 함께 받는다
const cumulativeStatWeight = (level: number, grade: WangapGrade): number => {
  let w = 0;
  for (let n = 1; n <= level; n++) w += statWeight(n);
  if (grade !== '전설') w += PROMO_WEIGHT;
  if (grade === '고대') w += PROMO_WEIGHT;
  return w;
};

export const getWangapStats = (level: number, grade: WangapGrade): WangapStats => {
  const wMax = cumulativeStatWeight(WANGAP_MAX_LEVEL, '고대');
  const anchorRatio = 1 + cumulativeStatWeight(STAT_ANCHOR_LEVEL, '고대') / wMax;
  const ratio = (1 + cumulativeStatWeight(level, grade) / wMax) / anchorRatio;
  return {
    힘민지: Math.round(STAT_ANCHOR.힘민지 * ratio),
    체력: Math.round(STAT_ANCHOR.체력 * ratio),
    무기공격력: Math.round(STAT_ANCHOR.무기공격력 * ratio),
    기본공격력: Math.round(STAT_ANCHOR.기본공격력 * ratio),
  };
};

